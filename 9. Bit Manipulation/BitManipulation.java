public class BitManipulation {

    public static void main(String[] args) {
        System.out.println(2 & 4); // binary AND
        System.out.println(2 | 4); // binary OR
        System.out.println(5 ^ 6); // X-OR
        System.out.println(~5); // Bitwise One's Compliment
        System.out.println(5 << 2); // left shift
        System.out.println(5 >> 1); // right shift
    }
}