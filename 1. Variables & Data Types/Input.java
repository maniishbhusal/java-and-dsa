import java.util.*;

public class Input {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String input = sc.nextLine();
        System.out.println(input);

        int num = sc.nextInt();
        System.out.println(num);

        long phoneNum = sc.nextLong();
        System.out.println(phoneNum);
    }

}